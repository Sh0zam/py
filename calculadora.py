##Calculadora de todas as operações 
x = int(input("Primeiro Numero: "));
operacao = input("Operação: ");
y = int(input("Segundo Numero: "));

	
if operacao == "+":


	print("Seu Resultado é:", x + y);


elif operacao == "-":


	print("Seu Resultado é:", x - y)


elif operacao == "*":


	print ("Seu Resultado é:", x * y)


elif operacao == "/":


	print(" Seu resultado é:", x / y )


else:


	print('A operação é invalida')


